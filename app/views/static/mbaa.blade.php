        <div class="page-header">
            <h3>寓意 <small></small></h3>
        </div>
        <p>联谊会取名“晨钟”，既取自成语“晨钟暮鼓”之意，又来于唐代大诗人杜甫 《游龙门奉先寺》诗：“欲觉闻晨钟，令人发深省。”比喻时不可待，使人警醒觉悟之意。用在此处，寓以毕业社员应时刻心系晨光，情系母校，魂系祖国；应时刻珍惜时光，居安思危，反省自新；应时刻鞭策自己，自强不息，奋发前进。</p>
        <img src="http://mbaa.qiniudn.com/img/mbaa/img/08.jpg" alt="08残奥会合影" class="img-rounded center-block" style="width:80%;">
<p></p>
        <div class="page-header">
            <h3>目的 <small></small></h3>
        </div>
        <p>本联谊会在遵守中华人民共和国宪法和法律以及社会公德，服从八十中学和晨光文学社指导的前提下，以互相学习增进友谊，互相交流博取众长为主旨，开展毕业社员与在校社员的交流会，为他们搭建一个无障碍的沟通平台；同时也方便已经毕业的社员相互之间保持联系，构筑相互间的和谐氛围。用我们的力量，服务于八十中，服务于晨光文学社，切实践行“今天我以八十为荣，明天八十以我为荣”的口号。</p>
        <div class="page-header">
            <h3>性质 <small></small></h3>
        </div>
        <p>联谊会是在学校和文学社的坚强领导下，由辅导老师和一部分毕业社员共同发起，以我校文学社毕业生组成的健康向上充满时代和活力气息的学生组织。</p>
        <div class="page-header">
            <h3>宗旨 <small></small></h3>
        </div>
        <p>由于联谊会隶属于晨光文学社，故该会以晨光文学社“健康仁爱和谐进步”为宗旨。</p>
        <p>健康，这是针对每一个社员而言，就是文学社的活动围绕这个根本的人文精神展开，即处处体现身体健康、心理健康、心灵健康和情感健康，使学生人格健全，能够承受失败和挫折，自律个人欲望和情绪，以一种自信、乐观、坚强、平实、宽容、感恩的心态笑对人生！它既是社员的在文学社的开始也是最后的归宿。这是文学社对每位社员赋予的最终发展方向和根本希望。</p>
        <p>仁爱，我们讲的是社员与社员之间的关系。“仁者爱人”，突出的是在人与人之间应具有亲亲之心、爱人之心、正义之心、真诚之心、克己之心。是让每位社员在文学社这个大家庭里感受到一份浓浓的爱和温暖，让文学和和集体滋养我们的仁爱之心，真正做到所谓的“我爱人人，人人爱我”。</p>
        <p>和谐，我们侧重讲的是文学社大家庭的关系。建立一种交流之和谐、合作之和谐、团队之和谐、生活之和谐关系，即自然的、良好的、默契的人际氛围和团队精神。
</p>
        <p>进步，我们讲的是文学社社员个人、文学社整体、文学社与八十中、文学社与社会的关系。那就是首先我们每个社员通过文学社的生活获得自己的进步，从而通过个人的进步促进文学社的发展，通过文学社的发展促进八十中校园文化的进一步提升，反之八十中的发展又促进晨光进一步壮大。一句话，共同发展，共同进步。</p>
        <div class="page-header">
            <h3>口号 <small>温馨 珍重 谦卑 鞭策</small></h3>
        </div>
        <p>温馨，我们指的是联谊会这一整体所要达到的目的。联谊会是我们温馨的大家庭，每个人在当中都能够畅所欲言，表达自己的思想。每个人都能在交流当中找到自我的价值，明确自己前进的方向。彼此之间相互关心，相互爱护，不分你我，其乐融融。</p>
        <p>珍重，联谊会内部已经毕业的社员之间以及与晨光文学社在校社员之间的关系。“海内存知己，天涯若比邻”，我们已经毕业的老社员曾经在一起共同奋斗过，如今已经各奔东西，这份情值得我们去珍惜。而对现在还在校的晨光学子们，我们更要给予他们无微不至的关怀，让他们明白高中生活的美好，让他们学会珍惜身边的一切。</p>
        <p>谦卑，主要针对已经毕业的社员而言的。虽然我们已经毕业，或者说是略有成就，但是一定要怀着一颗谦卑的心，一颗感恩的心，用我们的力量，为学校，为晨光出一分力。</p>
        <p>鞭策，是说我们要时刻鞭策激励自己，要居安思危，不能贪图安逸。时刻以报答学校，报答晨光为己任，不断提高自己的品德修养，脚踏大地，仰望星空，塑造经典自己。</p>
        <div class="page-header">
            <h3>信念 <small>感恩 奉献 成功 幸福</small></h3>
        </div>
        <p>感恩，我们讲的是一种精神，是指无论是已经毕业出去的联谊会委员，还是晨光文学社在校的社员，都应当明白自己的根在哪里。我们的母校，我们的老师以及其他对我们有帮助的人，我们都应当怀着一颗感恩的心。无愧于我们的良心，无愧于我们是大写的人。</p>
        <p>奉献，可以是对社会，对学校，对家庭的贡献，不论大小，在于行动。一个人，毫不利己专门利人是不可能的，但是只知索取不知奉献是可耻的。通过奉献，我们可以找到自己的定位，找到自己的价值，这样的人生，才是有意义的。
</p>
        <p>成功，我们希望每一位晨光人，晨钟人，都能够通过文学社，通过联谊会，找到自己人生的轨迹，明确自己的目标并为之奋斗，最终走向成功，成为国家的栋梁。</p>
        <p>幸福，赠人玫瑰，手有余香，我们希望在这个大家庭中的每一位成员都能够感觉到幸福，都能够给予别人以幸福。为别人而幸福，是幸福的最高境界，也应当是我们每一位成员所达到的。</p>
